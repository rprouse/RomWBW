SCSI_SR_CSD	.EQU	$00		; CURRENT SCSI DATA
SCSI_SR_ICR	.EQU	$01		; INITIATOR COMMAND
SCSI_SR_MR	.EQU	$02		; MODE
SCSI_SR_TCR	.EQU	$03		; TARGET COMMAND
SCSI_SR_CSBS	.EQU	$04		; CURRENT SCSI BUS STAT
SCSI_SR_BSR	.EQU	$05		; BUS STATUS
SCSI_SR_IDR	.EQU	$06		; INITIATOR DMA RECV
SCSI_SR_RPI	.EQU	$07		; RESET PARITY/IRQ
;
; NCR 5380 OUTPUT ONLY REGISTERS
;
SCSI_SR_ODR	.EQU	$00		; OUTPUT DATA
SCSI_SR_SER	.EQU	$04		; SELECT ENABLE
SCSI_SR_SDS	.EQU	$05		; START DMA SEND
SCSI_SR_SDTR	.EQU	$06		; START TARGET DMA RECV
SCSI_SR_SDIR	.EQU	$07		; START INITIATOR DMA RECV
;
#IF (SCSIMODE == SCSIMODE_LATCH)
SCSI_REGSEL	.EQU	SCSIBASE	; SCSI CHIP REGISTER SELECTION
SCSI_PDMA	.EQU	SCSIBASE + $01	; SCSI PSEUDO-DMA I/O
SCSI_DATA	.EQU	SCSIBASE + $02	; SCSI CHIP I/O
SCSI_CTL	.EQU	SCSIBASE + $03	; SCSI CONTROL & STATUS
