;
SCSI_CFG_IOBASE		.EQU	16		; IO BASE ADDRESS (BYTE)
SCSI_CFG_TGT		.EQU	17		; TARGET DEVICE ID
SCSI_CFG_TID		.EQU	18		; TARGET DEVICE BIT PATTERN
SCSI_CFG_LUN		.EQU	19		; TARGET LUN
;
SCSI_CFGSIZ		.EQU	20		; SIZE OF CFG TBL ENTRIES
;
;--------------------------------------------------------------------------------------------------
;   HBIOS MODULE HEADER
;--------------------------------------------------------------------------------------------------
;
ORG_SCSI	.EQU	$
;
	.DW	SIZ_SCSI		; MODULE SIZE
	.DW	SCSI_INITPHASE		; ADR OF INIT PHASE HANDLER
;
SCSI_INITPHASE:
	; INIT PHASE HANDLER, A=PHASE
	CP	HB_PHASE_PREINIT	; PREINIT PHASE?
	JP	Z,SCSI_PREINIT		; DO PREINIT
	CP	HB_PHASE_INIT		; INIT PHASE?
	JP	Z,SCSI_INIT		; DO INIT
	RET				; DONE
;
SCSI_CFGTBL:
;
#IF (SCSICNT >= 1)
;
