#ENDIF
;
#IF (SCSICNT >= 2)
	.DW	SCSI_FNTBL		; DRIVER FUNCTION TABLE ADDRESS
	.DB	'D'			; DRIVER TYPE
	.DB	0			; HBIOS UNIT NUMBER (FILLED DYNAMICALLY)
	.DB	1			; DRIVER UNIT NUMBER
