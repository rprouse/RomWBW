#IF ($ - SCSI_CFGTBL) != (SCSICNT * SCSI_CFGSIZ)
	.ECHO	"*** INVALID SCSI CONFIG TABLE ***\n"
	!!!	; FORCE AN ASSEMBLY ERROR
#ENDIF
;
	.DB	$FF			; END OF TABLE MARKER
;
;=============================================================================
; PRE-INITIALIZATION ENTRY POINT
;=============================================================================
;
SCSI_PREINIT:
	CALL	SCSI_DETECT
	LD	(SCSI_PRESENT),A
	RET
;
;=============================================================================
; INITIALIZATION ENTRY POINT
;=============================================================================
;
SCSI_INIT:
	CALL	NEWLINE			; FORMATTING
	PRTX(SCSI_STR_TAG)		; PRINT DRIVER NAME
	CALL	PC_COLON		; FORMATTING
;
	PRTS(" IO=0x$")			; LABEL FOR IO ADDRESS
	LD	A,SCSIBASE		; GET IO BASE ADDRES
	CALL	PRTHEXBYTE		; DISPLAY IT
;
	PRTS(" MODE=$")			; LABEL FOR MODE
	LD	A,SCSIMODE		; INDEX STRING TABLE BY MODE VAL
	LD	HL,SCSI_MODE_STR_TBL	; STRING TABLE ADDRESS
	CALL	PRTLKUP			; PRINT INDEXED STRING
;
#IF (SCSIPDMA)
	PRTS(" PDMA$")
#ENDIF
;
	LD	A,(SCSI_PRESENT)	; GET PRESENCE STATUS
	OR	A			; SET FLAGS
	JR	Z,SCSI_INIT1		; IF PRESENT, CONTINUE
	PRTS(" NOT PRESENT$")		; SHOW NOT PRESENT
	XOR	A			; SUCCESS
	RET				; AND BAIL OUT
;
SCSI_INIT1:
	LD	IY,SCSI_CFGTBL		; POINT TO START OF CONFIG TABLE
;
SCSI_INIT2:
	LD	A,(IY)			; LOAD FIRST BYTE TO CHECK FOR END
	CP	$FF			; CHECK FOR END OF TABLE VALUE
	RET	Z			; RETURN IF DONE
;
	CALL	SCSI_INIT3		; REGISTER & INIT DEVICE
;
	LD	DE,SCSI_CFGSIZ		; SIZE OF CFG TABLE ENTRY
	ADD	IY,DE			; BUMP POINTER
	JP	SCSI_INIT2		; AND LOOP
;
SCSI_INIT3:
	; ADD UNIT TO GLOBAL DISK UNIT TABLE
	LD	BC,SCSI_FNTBL		; BC := FUNC TABLE ADR
	PUSH	IY			; CFG ENTRY POINTER
	POP	DE			; COPY TO DE
	CALL	DIO_ADDENT		; ADD ENTRY TO GLOBAL DISK DEV TABLE
	LD	(IY+CFG_UNIT),A		; RECORD HBIOS UNIT NUMBER ASSIGNED
;
	CALL	SCSI_INITDEV		; INITIALIZE DEVICE
	PUSH	AF			; SAVE INIT STATUS
;
	CALL	DRV_PRTTAG		; TAG FOR ACTIVE DEVICE
	PRTS(" TGT=$")			; LABEL FOR TARGET DEVICE
	LD	A,(IY+SCSI_CFG_TGT)	; GET TARGET DEVICE
	CALL	PRTDEC8			; DISPLAY IT
;
	PRTS(" LUN=$")			; LUN LABEL
	LD	A,(IY+SCSI_CFG_LUN)	; LOAD LUN
	CALL	PRTDEC8			; DISPLAY IT
;
	POP	AF			; RECOVER INIT STATUS
	JR	Z,SCSI_INIT4		; IF Z, CONTINUE
	JP	ERR_PRT_SP		; ELSE ERROR TEXT AND ABORT
;
SCSI_INIT4:
	; PRINT STORAGE CAPACITY (BLOCK COUNT)
	PRTS(" BLOCKS=0x$")		; PRINT FIELD LABEL
	LD	A,CFG_DSKCAP		; OFFSET TO CAPACITY FIELD
	CALL	LDHLIYA			; HL := IY + A, REG A TRASHED
	CALL	LD32			; GET THE CAPACITY VALUE
	CALL	PRTHEX32		; PRINT HEX VALUE
;
	; PRINT STORAGE SIZE IN MB
	PRTS(" SIZE=$")			; PRINT FIELD LABEL
	LD	B,11			; 11 BIT SHIFT TO CONVERT BLOCKS --> MB
	CALL	SRL32			; RIGHT SHIFT
	CALL	PRTDEC32		; PRINT DWORD IN DECIMAL
	PRTS("MB$")			; PRINT SUFFIX
;
	; PRINT INQUIRY DATA (STILL IN HB_WRKBUF FROM INITDEV)
	CALL	PC_SPACE		; FORMATTING
	CALL	PC_LBKT			; FORMATTING
	LD	HL,HB_WRKBUF+8		; START OF ASCII DATA
	LD	B,28			; VENDOR, PRODUCT, AND REVISTION FIELDS
SCSI_INIT5:
	LD	A,(HL)			; LOAD NEXT CHAR
	CALL	COUT			; PRINT IT
	INC	HL			; BUMP BUFFER POINTER
	DJNZ	SCSI_INIT5		; LOOP AS NEEDED
	CALL	PC_RBKT			; FORMATTING
;
	XOR	A			; SUCCESS
	RET
;
;=============================================================================
; DRIVER FUNCTION TABLE
;=============================================================================
;
SCSI_FNTBL:
	.DW	SCSI_STATUS
	.DW	SCSI_RESET
	.DW	SCSI_SEEK
	.DW	SCSI_READ
	.DW	SCSI_WRITE
	.DW	SCSI_VERIFY
	.DW	SCSI_FORMAT
	.DW	SCSI_DEVICE
	.DW	SCSI_MEDIA
	.DW	SCSI_DEFMED
	.DW	SCSI_CAP
	.DW	SCSI_GEOM
#IF (($ - SCSI_FNTBL) != (DIO_FNCNT * 2))
	.ECHO	"*** INVALID SCSI FUNCTION TABLE ***\n"
#ENDIF
;
SCSI_VERIFY:
SCSI_FORMAT:
SCSI_DEFMED:
	LD	A,ERR_NOTIMPL		; NOT IMPLEMENTED
	OR	A
	RET
;
;
;
SCSI_READ:
	CALL	HB_DSKREAD_NEW		; HOOK HBIOS DISK READ SUPERVISOR
	LD	E,SCSI_CMD_READ		; SETUP READ COMMAND
	JP	SCSI_IO			; DO THE IO
;
;
;
SCSI_WRITE:
	CALL	HB_DSKWRITE_NEW		; HOOK HBIOS DISK WRITE SUPERVISOR
	LD	E,SCSI_CMD_WRITE	; SETUP WRITE COMMAND
	JP	SCSI_IO			; DO THE IO
;
;
;
SCSI_STATUS:
	LD	A,(IY+CFG_STATUS)	; GET STATUS OF SELECTED DEVICE
	OR	A			; SET FLAGS
	RET				; AND RETURN
;
;
;
SCSI_RESET:
	JP	SCSI_INITDEV		; REINITIALIZE UNIT AND RETURN
;
;
;
SCSI_DEVICE:
	LD	D,DIODEV_SCSI		; D := DEVICE TYPE
	LD	E,(IY+CFG_DEVNUM)	; E := PHYSICAL DEVICE NUMBER
	LD	C,%00110000		; C := GENERIC HARD DISK ATTRIBUTES
	LD	H,0			; H := MODE
	LD	L,(SCSI_CFG_IOBASE)	; L := BASE I/O ADDRESS
	XOR	A			; SIGNAL SUCCESS
	RET
;
;
;
SCSI_MEDIA:
	; CHECK IF MEDIA DISCOVERY REQUESTED
	BIT	0,E			; IF BIT 0 OF E, CHECK MEDIA
	JR	Z,SCSI_MEDIA_Z		; IF NOT SET, SKIP CHECK
;
	; CHECK CURRENT DEVICE STATUS
	LD	A,(IY+CFG_STATUS)	; GET OUR CURRENT STATUS
	OR	A			; SET FLAGS
	JR	NZ,SCSI_MEDIA1		; IF NOT OK, TRY INITDEV
;
	; USE INQUIRY COMMAND TO CHECK DEVICE/MEDIA
	CALL	SCSI_INQUIRY		; PERFORM INQUIRY COMMAND
	JR	Z,SCSI_MEDIA_Z		; IF OK, GOOD EXIT
	; ELSE FALL THRU TO TRY INITDEV TO RECOVER
;
SCSI_MEDIA1:
	; ATTEMPT TO RECOVER FROM ERROR STATE USING INITDEV
	CALL	SCSI_INITDEV		; PERFORM INIT
;
SCSI_MEDIA_Z:
	LD	A,(IY+CFG_STATUS)	; GET CURRENT STATUS
	OR	A			; SET FLAGS
	LD	D,0			; SIGNAL NO MEDIA CHANGE DETECTED
	LD	E,MID_HD		; ASSUME HARD DISK MEDIA
	RET	Z			; RETURN IF GOOD
	LD	E,MID_NONE		; ELSE SIGNAL NO MEDIA
	RET				; AND RETURN ERROR
;
;
;
SCSI_SEEK:
	PUSH	HL
	LD	A,CFG_DSKADR
	CALL	LDHLIYA
	EX	(SP),HL
	POP	BC
	CALL	ST32
	XOR	A			; SIGNAL SUCCESS
	RET				; AND RETURN
;
;
;
SCSI_CAP:
	LD	A,(IY+CFG_STATUS)	; GET STATUS
	PUSH	AF			; SAVE IT
	LD	A,CFG_DSKCAP		; OFFSET TO CAPACITY FIELD
	CALL	LDHLIYA			; HL := IY + A, REG A TRASHED
	CALL	LD32			; GET THE CURRENT CAPACITY INTO DE:HL
	LD	BC,512			; 512 BYTES PER BLOCK
	POP	AF			; RECOVER STATUS
	OR	A			; SET FLAGS
	RET
;
;
;
SCSI_GEOM:
	; FOR LBA, WE SIMULATE CHS ACCESS USING 16 HEADS AND 16 SECTORS
	; RETURN HS:CC -> DE:HL, SET HIGH BIT OF D TO INDICATE LBA CAPABLE
	CALL	SCSI_CAP		; GET TOTAL BLOCKS IN DE:HL, BLOCK SIZE TO BC
	LD	L,H			; DIVIDE BY 256 FOR # TRACKS
	LD	H,E			; ... HIGH BYTE DISCARDED, RESULT IN HL
	LD	D,16 | $80		; HEADS / CYL = 16, SET LBA CAPABILITY BIT
	LD	E,16			; SECTORS / TRACK = 16
	RET				; DONE, A STILL HAS SCSI_CAP STATUS
;
;=============================================================================
; FUNCTION SUPPORT ROUTINES
;=============================================================================
;
; ON RETURN, ZF SET INDICATES HARDWARE FOUND
;
SCSI_DETECT:
	LD	A,$05			; FIRST TEST VALUE
	SCSI_OUT(SCSI_SR_TCR)		; WRITE TO TCR REG
	SCSI_IN(SCSI_SR_TCR)		; READ BACK
	SUB	$05			; CORRECT?
	RET	NZ			; BAIL OUT IF NOT
	LD	A,$0A			; SECOND TEST VALUE
	SCSI_OUT(SCSI_SR_TCR)		; WRITE TO TCR REG
	SCSI_IN(SCSI_SR_TCR)		; READ BACK
	SUB	$0A			; CORRECT?
	RET				; RETURN WITH ZF INDICATING PRESENCE
;
; INITIALIZE DEVICE
; RETURN HBIOS RESULT CODE
;
SCSI_INITDEV:
;
	; RESET 5380
	XOR	A			; ZERO ACCUM
	SCSI_OUT(SCSI_SR_MR)		; RESET CONTROL REG
	SCSI_OUT(SCSI_SR_TCR)		; CLEAR TGT CMD REG
	SCSI_OUT(SCSI_SR_ICR)		; CLEAR INIT CMD REG
;
	; TRY TEST UNIT READY TO SEE IF SOMETHING
	; IS RESPONDING.  ANYTHING OTHER THAN TIMEOUT MEANS THERE
	; IS SOMETHING RESPONDING.
	CALL	SCSI_TSTRDY		; TEST UNIT READY
	CP	$1E			; TIMEOUT?
	JR	NZ,SCSI_INITDEV1	; PROCEED IF NOT
;
	;; RESET THE SCSI BUS
	;LD	DE,20			; 20 * 16US = 320US
	;CALL	DELAY			; WAIT
	;LD	A,$80			; ASSERT RESET BIT
	;SCSI_OUT(SCSI_SR_ICR)		; SET IT
	;LD	DE,10			; 10 * 16US = 160US
	;CALL	DELAY			; WAIT
	;XOR	A			; CLEAR RESET BIT
	;SCSI_OUT(SCSI_SR_ICR)		; CLEAR RESET
	;CALL	LDELAY			; DELAY ABOUT 500MS (REQUIRED)
;
	; TRY TEST UNIT READY AGAIN
	CALL	SCSI_TSTRDY		; TEST UNIT READY
	CP	$1E			; TIMEOUT?
	JR	NZ,SCSI_INITDEV1	; PROCEED IF NOT
	JP	SCSI_ERR		; HANDLE ERROR AND RETURN
;
SCSI_INITDEV1:
	; SEND A START UNIT COMMAND IN CASE DRIVE NEEDS IT
	CALL	SCSI_START		; PERFORM START UNIT
	JP	NZ,SCSI_ERR		; HANDLE ERROR AND RETURN
;
SCSI_INITDEV2:
	; NOW LOOP ON TEST UNIT READY UNTIL GOOD STATUS.
	; IF A TIMEOUT OCCURS, WE ABORT.  OTHERWISE, WE LOOP
	; FOREVER WAITING FOR DEVICE TO BE READY.  A LITTLE
	; DANGEROUS, BUT SHOULD NOT BE A REAL PROBLEM.
	; THIS SHOULD BE IMPROVED TO CHECK SENSE DATA TO CONFIRM
	; DEVICE IS ACTUALLY "BECOMING READY"
	CALL	SCSI_TSTRDY		; TEST UNIT READY
	CP	$1E			; TIMEOUT?
	JP	Z,SCSI_ERR		; HANDLE ERROR AND RETURN
	OR	A			; SET FLAGS
	JR	NZ,SCSI_INITDEV2	; LOOP ON ALL OTHER ERRORS
;
	; INQUIRY COMMAND TO CAPTURE DEVICE SPECIFICS
	CALL	SCSI_INQUIRY		; RUN THE COMMAND
	JP	NZ,SCSI_ERR		; HANDLE ERROR AND RETURN
;
	; GET DEVICE CAPACITY
	CALL	SCSI_CAPACITY		; GET MEDIA CAPACITY
	JP	NZ,SCSI_ERR		; HANDLE ERROR AND RETURN
;
	XOR	A			; SIGNAL OK
	LD	(IY+CFG_STATUS),A	; RESET DEVICE STATUS
	RET				; DONE
;
; PERFORM SECTOR READ/WRITE
; RETURNS HBIOS ERROR CODES
;
SCSI_IO:
	CALL	SCSI_CHKERR		; ATTEMPT TO RESET ERR STATUS
	RET	NZ			; ABORT IF ERROR ACTIVE
;
	; SETUP SCSI COMMAND
	LD	A,E
	LD	(SCSI_S_CDB),A		; SET SCSI COMMAND
;
	; SETUP DISK BUFFER POINTERS
	LD	(SCSI_BUF_OUT),HL	; INPUT BUFFER
	LD	(SCSI_BUF_IN),HL	; OUTPUT BUFFER
;
	; SETUP CDB
	LD	HL,SCSI_S_CDB		; SET HL TO R/W CBD
	LD	(SCSI_CUR_CDB),HL	; SAVE IN IOT
;
	; SETUP LUN/LBA
	LD	A,(IY+SCSI_CFG_LUN)	; PUT LUN IN ACCUM
	RRCA				; MOVE TO TOP 3 BITS
	RRCA				; ...
	RRCA				; ...
	LD	B,A			; SAVE IN B
	LD	A,CFG_DSKADR		; OFFSET OF LBA FIELD
	CALL	LDHLIYA			; HL := IY + A, REG A TRASHED
	CALL	LD32			; LD DE:HL,(HL)
	BIT	7,D			; CHECK FOR LBA (NOT CHS)
	JR	NZ,SCSI_IO1		; IF LBA, PROCEED
	LD	A,ERR_RANGE		; HANDLE AS RANGE ERROR
	OR	A			; SET FLAGS
	RET				; AND ABORT
SCSI_IO1:
	CALL	HB_DSKACT		; SHOW ACTIVITY
	LD	A,E			; MSB OF LBA
	AND	%00011111		; CLEAR LUN BITS	
	OR	B			; MERGE WITH LUN
	LD	(SCSI_CUR_LUN),A	; PUT IN CDB
	LD	A,H			; LBA MID
	LD	(SCSI_CUR_LBN),A	; PUT IN CDB
	LD	A,L			; LBA LOW
	LD	(SCSI_CUR_LBN+1),A	; PUT IN CDB
;
	; DO THE SCSI TRANSACTION
	CALL	SCSI_GO			; DO THE SCSI TRANSACTION
	JP	NZ,SCSI_ERR		; HANDLE ERROR
;
SCSI_IO2:
	; INCREMENT LBA
	LD	A,CFG_DSKADR		; LBA OFFSET
	CALL	LDHLIYA			; HL := IY + A, REG A TRASHED
	CALL	INC32HL			; INCREMENT THE VALUE
;
	XOR	A			; SIGNAL SUCCESS
	RET
;
; CHECK CURRENT DEVICE FOR ERROR STATUS AND ATTEMPT TO RECOVER
; VIA RESET IF DEVICE IS IN ERROR.
;
SCSI_CHKERR:
	LD	A,(IY+CFG_STATUS)	; GET STATUS
	OR	A			; SET FLAGS
	RET	Z			; JUST RET IF NO ERROR
	PUSH	DE			; SAVE COMMAND BYTE
	PUSH	HL			; SAVE BUFFER ADDRESS
	CALL	SCSI_INITDEV		; IF ERROR STATUS, TRY TO REINIT
	POP	HL			; RESTORE BUFFER ADDRESS
	POP	DE			; RESTORE COMMAND BYTE
	RET
;
; ISSUE SCSI INQUIRY COMMAND
; ON INPUT, HL POINTS TO OUTPUT DATA BUFFER
; RETURNS SCSI STATUS CODE
;
SCSI_INQUIRY:
	; SETUP BUFFER POINTERS
	LD	HL,HB_WRKBUF		; BUFFER FOR INQUIRY DATA
	LD	(SCSI_BUF_OUT),HL	; INPUT BUFFER
	LD	(SCSI_BUF_IN),HL	; OUTPUT BUFFER
	; SETUP INQUIRY CDB
	LD	HL,SCSI_S_INQ		; SET HL TO INQ CBD
	LD	(SCSI_CUR_CDB),HL	; SAVE IN IOT
	; SETUP LUN
	LD	A,(IY+SCSI_CFG_LUN)	; PUT LUN IN ACCUM
	RRCA				; MOVE TO TOP 3 BITS
	RRCA				; ...
	RRCA				; ...
	LD	(SCSI_INQ_LUN),A	; PUT IN INQ CDB
	; DO THE SCSI TRANSACTION
	JR	SCSI_GO			; DO THE SCSI TRANSACTION
;
; ISSUE SCSI CAPACITY COMMAND AND CAPTURE
; RESULTS AS DEVICE CAPACITY
; RETURNS SCSI STATUS CODE
;
SCSI_CAPACITY:
	; SETUP BUFFER POINTERS
	LD	HL,SCSI_CAP_BUF
	LD	(SCSI_BUF_OUT),HL	; INPUT BUFFER
	LD	(SCSI_BUF_IN),HL	; OUTPUT BUFFER
	; SETUP READ CAPACITY CDB
	LD	HL,SCSI_S_CAP		; SET HL TO CAP CBD
	LD	(SCSI_CUR_CDB),HL	; SAVE IN IOT
	; SETUP LUN
	LD	A,(IY+SCSI_CFG_LUN)	; PUT LUN IN ACCUM
	RRCA				; MOVE TO TOP 3 BITS
	RRCA				; ...
	RRCA				; ...
	LD	(SCSI_CAP_LUN),A	; PUT IN CAP CDB
	; DO THE SCSI TRANSACTION
	CALL	SCSI_GO			; DO THE SCSI TRANSACTION
	RET	NZ
;
	; RETURNED LBA IS BIG ENDIAN.  CONVERT TO A STANDARD 32-BIT
	; INTEGER IN DE:HL
	LD	HL,SCSI_CAP_BUF		; HL POINTS TO START OF BUF
	LD	D,(HL)			; MSB
	INC	HL			; NEXT BYTE
	LD	E,(HL)
	PUSH	DE			; SAVE HIGH WORD
	INC	HL
	LD	D,(HL)
	INC	HL
	LD	E,(HL)			; LSB
	POP	HL			; RECOVER HIGH WORD
	EX	DE,HL			; AND SWAP
;
	; RETURNED CAPACITY IS THE *LAST* LBA.  WE WANT THE NUMBER OF
	; BLOCKS AVAILABLE, SO WE NEED TO INCREMENT THE RETURNED VALUE.
	CALL	INC32
;
	; COPY CAPACITY TO DEVICE CONFIG DATA FIELD
	PUSH	HL			; SAVE HL
	LD	A,CFG_DSKCAP		; OFFSET OF MEDIA CAPACITY FIELD		
	CALL	LDHLIYA			; HL POINTS TO MEDIA CAPACITY FIELD
	PUSH	HL			; MOVE HL
	POP	BC			; ... TO BC
	POP	HL			; RECOVER HL
	CALL	ST32			; SAVE CAP TO CFG BLOCK
;
	XOR	A
	RET				; DONE
;
; ISSUE SCSI SENSE COMMAND AND CAPTURE RESULTS
; RETURNS SCSI STATUS CODE
;
SCSI_SENSE:
	; SETUP BUFFER POINTERS
	LD	HL,SCSI_SENSE_BUF
	LD	(SCSI_BUF_OUT),HL	; INPUT BUFFER
	LD	(SCSI_BUF_IN),HL	; OUTPUT BUFFER
	; SETUP SENSE CDB
	LD	HL,SCSI_S_SENSE		; SET HL TO SENSE CDB
	LD	(SCSI_CUR_CDB),HL	; SAVE IN IOT
	; DO THE SCSI TRANSACTION
	JR	SCSI_GO			; DO THE SCSI TRANSACTION
;
; ISSUE SCSI START UNIT COMMAND
; RETURNS SCSI STATUS CODE
;
SCSI_START:
	; SETUP BUFFER POINTERS FOR SAFETY EVEN THOUGH THIS
	; COMMAND DOES NOT TRANSFER ANY DATA
	LD	HL,HB_WRKBUF
	LD	(SCSI_BUF_OUT),HL	; INPUT BUFFER
	LD	(SCSI_BUF_IN),HL	; OUTPUT BUFFER
	; SETUP SENSE CDB
	LD	HL,SCSI_S_START		; SET HL TO START CDB
	LD	(SCSI_CUR_CDB),HL	; SAVE IN IOT
	; DO THE SCSI TRANSACTION
	JR	SCSI_GO			; DO THE SCSI TRANSACTION
;
; ISSUE SCSI TEST UNIT READY COMMAND
; RETURNS SCSI STATUS CODE
;
SCSI_TSTRDY:
	; SETUP BUFFER POINTERS FOR SAFETY EVEN THOUGH THIS
	; COMMAND DOES NOT TRANSFER ANY DATA
	LD	HL,HB_WRKBUF
	LD	(SCSI_BUF_OUT),HL	; INPUT BUFFER
	LD	(SCSI_BUF_IN),HL	; OUTPUT BUFFER
	; SETUP TEST UNIT READY CDB
	LD	HL,SCSI_S_TSTRDY	; SET HL TO START CDB
	LD	(SCSI_CUR_CDB),HL	; SAVE IN IOT
	; DO THE SCSI TRANSACTION
	JR	SCSI_GO			; DO THE SCSI TRANSACTION
;
;=============================================================================
; COMMAND PROCESSING
;=============================================================================
;
; WRAPPER FOR HBIOS INVOCATION
; RETURNS SCSI STATUS CODE
;
SCSI_GO:
	PUSH	IX			; PRESERVE IX
	LD	IX,SCSI_S_IOT		; POINT TO IO TABLE
	LD	A,(IY+SCSI_CFG_TID)	; GET TARGET ID
	LD	(IX),A			; PUT IN IOT
	CALL	SCSI_RUN		; RUN THE TRANSACTION
	POP	IX			; RESTORE IX
#IF (SCSITRACE >= 2)
	CALL	SCSI_DUMPCMD		; DISPLAY COMMAND
#ENDIF
	AND	%00011110		; IGNORE RESERVED BITS OF STATUS
	RET				; AND RETURN
;
;-------------------------------------------------------------------------------
; RUN THE SCSI TRANSACTION
;-------------------------------------------------------------------------------
;
SCSI_RUN:
	LD	L,(IX+4)		; POINT TO STATUS ADDRESS
	LD	H,(IX+5)		; POINT TO STATUS ADDRESS
	LD	(HL),$FF		; PRESET ERROR STATUS
;
	XOR	A			;
	SCSI_OUT(SCSI_SR_MR)		; RESET CONTROL REG
	SCSI_OUT(SCSI_SR_TCR)		; CLEAR COMMAND REG.
	SCSI_OUT(SCSI_SR_ICR)		;
	LD	A,(IX)			; GET TID
	BIT	7,A			;    TURN ON MY ID
	SCSI_OUT(SCSI_SR_ODR)		; SET TARGET ID
	LD	A,$01			; DRIVE BUS
	SCSI_OUT(SCSI_SR_ICR)		;
	LD	A,$05			; SET SEL AND DRIVE BUS
	SCSI_OUT(SCSI_SR_ICR)		;
;
; WAIT FOR SCSI BUSY
	LD	BC,5000			; TIMEOUT
S_SWT:
	SCSI_IN(SCSI_SR_CSBS)		; GET BUS STATUS
	AND	SCSI_SM_BSY		; MASK TO BUSY BIT
	JR	NZ,S_BSYA		; JMP IF BUSY ACTIVE
	CALL	DELAY			; KILL A LITTLE TIME
	DEC	BC			; DEC TIMEOUT
	LD	A,B			; TEST TIMEOUT=0
	OR	C
	JR	NZ,S_SWT		; JMP IF NO TIMEOUT
	XOR	A			; CLEAR A
	SCSI_OUT(SCSI_SR_ICR)		; CLEAR SEL AND DATA
	JR	S_EXIT			; ABORT SCSI PROTOCOL
;
;BUSY IS ACTIVE
S_BSYA:
	XOR	A			; CLEAR A
	SCSI_OUT(SCSI_SR_ICR)		; CLEAR SEL AND DATA
;
;-------------------------------------------------------------------------------
;
; TARGET IS SELECTED SO TEST BSY AND REQ
; THEN DECODE REQUESTED PHASE
;
;-------------------------------------------------------------------------------
PHASE:
	XOR	A
	SCSI_OUT(SCSI_SR_ICR)		; RESET INITIATOR
;
S_WTREQ:
	SCSI_IN(SCSI_SR_CSBS)		; GET BUS STATUS
	LD	B,A			; SAVE COPY
	AND	SCSI_SM_BSY		; MASK TO BUSY BIT
	JR	NZ,S_WTRQ1		; STILL BUSY SO CONT.
;
;-------------------------------------------------------------------------------
; BUSY LOST SO EXIT
;-------------------------------------------------------------------------------
S_EXIT:
	LD	L,(IX+4)		; GET STATUS POINTER
	LD	H,(IX+5)		; GET STATUS POINTER
	LD	A,(HL)			; LOAD STATUS IN ACCUM
	RET				; BUSY LOST? WERE DONE
;
S_WTRQ1:
	LD	A,B			; RECOVER STATUS
	AND	SCSI_SM_REQ		; REQUEST ACTIVE?
	JR	Z,S_WTREQ		; LOOP TILL REQUEST
;
;-------------------------------------------------------------------------------
; REQUEST IS ACTIVE SO SET 5380 TO REQUESTED PHASE
;-------------------------------------------------------------------------------
	LD	A,B			; RECOVER BUS STATUS
	RR	A			; SHIFT STATUS TO PHASE BITS
	RR	A
	AND	$07			; MASK TO PHASE BITS
	SCSI_OUT(SCSI_SR_TCR)		; SET PHASE
;
; JUMP TO PHASE HANDLER
;
	LD	A,B			; RECOVER BUS STATUS 
	RR	A
	AND	$0E			; MASK TO PHASE BITS
	LD	E,A			;
	LD	D,0
	LD	HL,PHTABL		; POINT TO TABLE
	ADD	HL,DE			; OFFSET INTO TABLE
	LD	E,(HL)			; GET EXECUTION ADDRESS
	INC	HL
	LD	D,(HL)
	PUSH	DE
	RET				; EXECUTE PHASE
;
;-------------------------------------------------------------------------------
; PHASE TABLE
;-------------------------------------------------------------------------------
;
PHTABL:
	.WORD	PHASE0			; DATA OUT
	.WORD	PHASE1			; DATA IN
	.WORD	PHASE2			; CMD OUT
	.WORD	PHASE3			; STATUS IN
	.WORD	S_EXIT			; UNUSED
	.WORD	S_EXIT			; UNUSED
	.WORD	PHASE6			; MESSAGE OUT
	.WORD	PHASE7			; MESSAGE IN
;
;-------------------------------------------------------------------------------
;PHASE HANDLERS
;-------------------------------------------------------------------------------
;
; DATA OUT
PHASE0:
	LD	L,(IX+6)		; GET POINTER
	LD	H,(IX+7)
#IF (SCSITRACE >= 3)
	CALL	DRV_PRTTAG
	PRTS(" DOUT$")
#ENDIF
	JP	S_WRIT			; WRITE BYTES
;
; DATA IN
PHASE1:
	LD	L,(IX+8)		; GET POINTER
	LD	H,(IX+9)
#IF (SCSITRACE >= 3)
	CALL	DRV_PRTTAG
	PRTS(" DIN$")
#ENDIF
	JP	S_READ			; READ BYTES
;
; CMD OUT
PHASE2:
	LD	L,(IX+2)
	LD	H,(IX+3)
#IF (SCSITRACE >= 3)
	CALL	DRV_PRTTAG
	PRTS(" CMD$")
#ENDIF
	JP	S_WRIT
;
; STATUS IN
PHASE3:
	LD	L,(IX+4)
	LD	H,(IX+5)
#IF (SCSITRACE >= 3)
	CALL	DRV_PRTTAG
	PRTS(" STIN$")
#ENDIF
	JP	S_READ
;
; MSG OUT
PHASE6:
	LD	L,(IX+10)
	LD	H,(IX+11)
#IF (SCSITRACE >= 3)
	CALL	DRV_PRTTAG
	PRTS(" MSGOUT$")
#ENDIF
	JP	S_WRIT
;
; MSG IN
PHASE7:
	LD	L,(IX+12)
	LD	H,(IX+13)
#IF (SCSITRACE >= 3)
	CALL	DRV_PRTTAG
	PRTS(" MSGIN$")
#ENDIF
	JP	S_READ
;
;=============================================================================
; HARDWARE INTERFACE ROUTINES
;=============================================================================
;
#IF (SCSIPDMA == FALSE)
;
;-------------------------------------------------------------------------------
; SCSI WRITE ROUTINE (SEND BYTES TO TARGET)
; BUS PHASE ALREADY SET. RETURN ON PHASE MISMATCH 
;-------------------------------------------------------------------------------
S_WRIT:
	LD	A,1			; DRIVE SCSI DATA BUS
	SCSI_OUT(SCSI_SR_ICR)
;
; WAIT FOR REQ WHILE CHECKING BUSY
;
S_WWREQ:
	SCSI_IN(SCSI_SR_CSBS)		; GET CURRENT BUS STAT
	LD	B,A			; SAVE IT
	AND	SCSI_SM_REQ		; REQUEST ACTIVE?
	JR	NZ,S_WNXT		;
;
	LD	A,B			; RECOVER CURRENT BUS STAT
	AND	SCSI_SM_BSY		; STILL BUSY?
	JP	NZ,S_WWREQ		; LOOP IF STILL BUSY
	JP	PHASE			;
;
; REQUEST ACTIVE SO CHECK PHASE
;
S_WNXT:
	SCSI_IN(SCSI_SR_BSR)		; GET STATUS
	AND	SCSI_SM_PHM		; MASK TO PHASE MATCH
	JP	Z,PHASE			; JMP IF PHASE MISMATCH
;
; REQUEST ACTIVE AND PHASE MATCH SO SEND DATA
;
	LD	A,(HL)			; GET BYTE
	SCSI_OUT(SCSI_SR_ODR)		; SEND DATA
#IF (SCSITRACE >= 3)
	CALL	PC_SPACE		;
	CALL	PRTHEXBYTE		;
#ENDIF
	LD	A,$11			; ACK + DRIVE BUS
	SCSI_OUT(SCSI_SR_ICR)		; SET ACK,DRIVE SCSI BUS
	INC	HL			; ADVANCE BUF POINTER
;
; WAIT FOR REQUEST TO DROP
;
S_WNREQ:
	SCSI_IN(SCSI_SR_CSBS)		; GET CURRENT STATUS
	AND	SCSI_SM_REQ
	JR	NZ,S_WNREQ		; LOOP TILL REQ DROPS
;
	LD	A,$1			; DROP ACK,ASSERT DATA
	SCSI_OUT(SCSI_SR_ICR)		; DROP ACK
	JP	S_WWREQ			; LOOP FOR NEXT BYTE/PHASE
;
#ELSE
;
;-------------------------------------------------------------------------------
; SCSI PSEUDO-DMA WRITE ROUTINE (SEND BYTES TO TARGET)
; BUS PHASE ALREADY SET. RETURN ON PHASE MISMATCH OR DMA REQ DROP
;-------------------------------------------------------------------------------
S_WRIT:
	LD	A,1			; DRIVE SCSI DATA BUS
	SCSI_OUT(SCSI_SR_ICR)
;
	;CALL	PC_LT			; *DEBUG*
	;CALL	SCSI_DUMP		; *DEBUG*
	LD	BC,0			; INIT BYTE RCV COUNT (FOR DEBUG)
	SCSI_IN(SCSI_SR_MR)		; GET CURRENT MODE REGISTER VALUE
	OR	%00000010		; SET DMA MODE
	SCSI_OUT(SCSI_SR_MR)		; SEND IT
	SCSI_OUT(SCSI_SR_SDS)		; START DMA INITIATOR SEND
;
; WAIT FOR REQ WHILE CHECKING BUSY
;
S_WWREQ:
	SCSI_IN(SCSI_SR_CSBS)		; GET CURRENT BUS STAT
	LD	E,A			; SAVE IT
	AND	SCSI_SM_REQ		; REQUEST ACTIVE?
	JR	NZ,S_WNXT		;
;
	LD	A,E			; RECOVER CURRENT BUS STAT
	AND	SCSI_SM_BSY		; STILL BUSY?
	JP	NZ,S_WWREQ		; LOOP IF STILL BUSY
	JP	S_WDONE			;
;
; REQUEST ACTIVE SO CHECK PHASE
;
S_WNXT:
	SCSI_IN(SCSI_SR_BSR)		; GET STATUS
	AND	SCSI_SM_PHM		; MASK TO PHASE MATCH
	JP	Z,S_WDONE		; JMP IF PHASE MISMATCH
;
	LD	A,(HL)			; GET NEXT BYTE
	INC	HL			; ADVANCE BUF POINTER
	OUT	(SCSI_PDMA),A		; WRITE DATA
#IF (SCSITRACE >= 3)
	CALL	PC_SPACE		;
	CALL	PRTHEXBYTE		;
#ENDIF
	INC	BC			; INC BYTE COUNT (FOR DEBUG)
	JR	S_WWREQ			; LOOP
;
; RESET MODE (CLEAR DMA MODE)
;
S_WDONE:
	;CALL	PC_GT			; *DEBUG*
	;CALL	PRTHEXWORD		; *DEBUG*
	IN	A,(SCSI_SR_MR)		; GET CURRENT MODE REGISTER VALUE
	AND	%11111101		; CLEAR DMA MODE
	OUT	(SCSI_SR_MR),A		; SEND IT
	;CALL	SCSI_DUMP		; *DEBUG*
	JP	PHASE			; BACK TO PHASE HANDLER
#ENDIF
;
#IF (SCSIPDMA == FALSE)
;
;-------------------------------------------------------------------------------
; SCSI READ ROUTINE (GET BYTES FROM TARGET)
; BUS PHASE ALREADY SET. RETURN ON PHASE MISMATCH 
;-------------------------------------------------------------------------------
;
S_READ:
;
; WAIT FOR REQ WHILE CHECKING BUSY
;
S_RWREQ:
	SCSI_IN(SCSI_SR_CSBS)		; GET CURRENT BUS STAT
	LD	B,A			; SAVE IT
	AND	SCSI_SM_REQ		; REQUEST ACTIVE?
	JR	NZ,S_RNXT		;
;
	LD	A,B			; RECOVER CURRENT BUS STAT
	AND	SCSI_SM_BSY		; STILL BUSY?
	JP	NZ,S_RWREQ		; LOOP IF STIL BUSY
	JP	PHASE			;
;
; REQUEST ACTIVE SO CHECK PHASE
;
S_RNXT:
	SCSI_IN(SCSI_SR_BSR)		; GET STATUS
	AND	SCSI_SM_PHM		; MASK TO PHASE MATCH
	JP	Z,PHASE			; JMP IF PHASE MISMATCH
;
; REQUEST ACTIVE AND PHASE MATCH SO GET DATA
;
	SCSI_IN(SCSI_SR_CSD)		; GET DATA
#IF (SCSITRACE >= 3)
	CALL	PC_SPACE		;
	CALL	PRTHEXBYTE		;
#ENDIF
	LD	(HL),A			; SAVE DATA
	LD	A,$10
	SCSI_OUT(SCSI_SR_ICR)		; SET ACK
	INC	HL			; ADVANCE BUF POINTER
;
;WAIT FOR REQUEST TO DROP
;
S_RNREQ:
	SCSI_IN(SCSI_SR_CSBS)		; GET CURRENT STATUS
	AND	SCSI_SM_REQ
	JR	NZ,S_RNREQ		; LOOP TILL REQ DROPS
	XOR	A
	SCSI_OUT(SCSI_SR_ICR)		; DROP ACK
	JP	S_READ			; LOOP FOR NEXT BYTE/PHASE
;
#ELSE
;
;-------------------------------------------------------------------------------
; SCSI PSEUDO-DMA READ ROUTINE (GET BYTES FROM TARGET)
; BUS PHASE ALREADY SET. RETURN ON PHASE MISMATCH OR DMA REQ DROP
;-------------------------------------------------------------------------------
;
S_READ:
	;CALL	PC_LT			; *DEBUG*
	;CALL	SCSI_DUMP		; *DEBUG*
	LD	BC,0			; INIT BYTE RCV COUNT (FOR DEBUG)
	SCSI_IN(SCSI_SR_MR)		; GET CURRENT MODE REGISTER VALUE
	OR	%00000010		; SET DMA MODE
	SCSI_OUT(SCSI_SR_MR)		; SEND IT
	SCSI_OUT(SCSI_SR_SDIR)		; START DMA INITIATOR RECEIVE
;
; WAIT FOR REQ WHILE CHECKING BUSY
;
S_RWREQ:
	SCSI_IN(SCSI_SR_CSBS)		; GET CURRENT BUS STAT
	LD	E,A			; SAVE IT
	AND	SCSI_SM_REQ		; REQUEST ACTIVE?
	JR	NZ,S_RNXT		;
;
	LD	A,E			; RECOVER CURRENT BUS STAT
	AND	SCSI_SM_BSY		; STILL BUSY?
	JP	NZ,S_RWREQ		; LOOP IF STIL BUSY
	JP	S_RDONE			;
;
; REQUEST ACTIVE SO CHECK PHASE
;
S_RNXT:
	SCSI_IN(SCSI_SR_BSR)		; GET STATUS
	AND	SCSI_SM_PHM		; MASK TO PHASE MATCH
	JP	Z,S_RDONE		; JMP IF PHASE MISMATCH
;
; REQUEST ACTIVE AND PHASE MATCH SO GET DATA
;
	IN	A,(SCSI_PDMA)		; GET DATA
#IF (SCSITRACE >= 3)
	CALL	PC_SPACE		;
	CALL	PRTHEXBYTE		;
#ENDIF
	LD	(HL),A			; SAVE DATA
	INC	HL			; ADVANCE BUF POINTER
	INC	BC			; INC BYTE COUNT (FOR DEBUG)
	JR	S_RWREQ			; LOOP
;
; RECEIVE FINAL BYTE
;
S_RDONE:
	IN	A,(SCSI_PDMA)		; GET FINAL BYTE
#IF (SCSITRACE >= 3)
	CALL	PC_SPACE		;
	CALL	PRTHEXBYTE		;
#ENDIF
	LD	(HL),A			; SAVE DATA
	INC	HL			; ADVANCE BUF POINTER
	INC	BC			; INC BYTE COUNT (FOR DEBUG)
;
; RESET MODE (CLEAR DMA MODE)
;
S_RDONE2:
	;CALL	PC_GT			; *DEBUG*
	;CALL	PRTHEXWORD		; *DEBUG*
	IN	A,(SCSI_SR_MR)		; GET CURRENT MODE REGISTER VALUE
	AND	%11111101		; CLEAR DMA MODE
	OUT	(SCSI_SR_MR),A		; SEND IT
	;CALL	SCSI_DUMP		; *DEBUG*
	JP	PHASE			; BACK TO PHASE HANDLER
#ENDIF
;
;=============================================================================
; ERROR HANDLING AND DIAGNOSTICS
;=============================================================================
;
; EVALUATE A SCSI STATUS BYTE, SET HBIOS ERROR
; THIS COULD BE IMPROVED BY LOOKING FOR STATUS=0x02 (CHECK CONDITION)
; AND, IF SO, USE REQUEST SENSE TO GET BETTER ERROR INDICATION.
;
SCSI_ERR:
#IF (SCSITRACE == 1)
	CALL	SCSI_DUMPCMD		; DISPLAY COMMAND
#ENDIF
	CP	$1E			; TRANSACTION NOT COMPLETED?
	JR	NZ,SCSI_ERR1		; HANDLE SOMETHING ELSE
	LD	A,ERR_NOTRDY		; TRANSLATE TO NOT READY ERROR
	LD	(IY+CFG_STATUS),A	; SET PERSISTENT STATUS
	JR	SCSI_ERR_Z		; FINISH UP
SCSI_ERR1:
	LD	A,ERR_IO		; ANYTHING ELSE IS AN IO ERROR
SCSI_ERR_Z:
	OR	A			; SET FLAGS
	RET				; AND RETURN
;
; DISPLAY PREVIOUS SCSI COMMAND AND RESULTANT STATUS BYTE
;
SCSI_DUMPCMD:
	CALL	DRV_PRTTAG		; FORMATTING
	PUSH	AF			; SAVE AF
	PUSH	HL			; SAVE HL
	LD	HL,(SCSI_CUR_CDB)	; POINT TO CDB
	LD	B,6			; COMMAND IS AT LEAST 6 BYTES
SCSI_DUMPCMD1:
	LD	A,(HL)			; LOAD NEXT BYTE
	INC	HL			; BUMP PTR
	CALL	PC_SPACE		; FORMATTING
	CALL	PRTHEXBYTE		; PRINT BYTE
	DJNZ	SCSI_DUMPCMD1		; LOOP THRU ALL CMD BYTES
	POP	HL			; RECOVER HL
	POP	AF			; RECOVER AF
	PRTS(" ==> $")			; FORMATTING
	CALL	PRTHEXBYTE		; PRINT STATUS BYTE
	RET
